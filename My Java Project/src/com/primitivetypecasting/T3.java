//WAP to design a method which can display all the values till a given limit
package com.primitivetypecasting;

public class T3 
{
public static void display(int n)
 {
	for (int i = 0; i <= n; i++) 
	{
		System.out.println(i);
	}
	
 }
	public static void main(String[] args) 
	{
	display(20);
	}

}
